package com.wahyusembiring.datetime.formatter

enum class FormattingStyle {

    /**
     * Example: 1 Jan 23
     */
    INDO_SHORT,

    /**
     * Example: 01 January 2023
     */
    INDO_MEDIUM,

    /**
     * Example: Monday, 01 January 2023
     */
    INDO_FULL,

    /**
     * Example: Mon, 1 Jan 23
     */
    INDO_FULL_SHORT,

}